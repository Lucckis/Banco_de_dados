-- 1 Consultas Tipo numérica (COUNT, MAX, MIN e etc.) 

select id_estacao, count(*) as numero_restaurantes
from t_restaurante
group by id_estacao
order by numero_restaurantes;

select id_estacao, max(media_avaliacao) as maior_avaliacao
from t_restaurante
group by id_estacao
order by maior_avaliacao desc;


select id_estacao, min(media_avaliacao) as menor_avaliacao
from t_restaurante
group by id_estacao
order by menor_avaliacao asc;

select id_estacao, avg(faixa_preco) media_de_preco
from t_restaurante
group by id_estacao
order by media_de_preco desc;



